namespace CoopSpectator.Network // Оголошуємо простір імен для мережевого шару моду
{ // Починаємо блок простору імен
    /// <summary> // Документуємо enum
    /// Роль поточного екземпляра гри в нашому TCP-з'єднанні. // Пояснюємо навіщо enum
    /// </summary> // Завершуємо XML-коментар
    public enum NetworkRole // Оголошуємо enum, щоб явно розрізняти режим роботи
    { // Починаємо блок enum
        None = 0, // Жодна роль (мережа не запущена)
        Server = 1, // Ми хостимо (слухаємо порт і приймаємо клієнтів)
        Client = 2 // Ми клієнт (підключаємося до хоста)
    } // Завершуємо блок enum
} // Завершуємо блок простору імен

